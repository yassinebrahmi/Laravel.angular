    }

    public function diarymain()
    {
        $diary = new Diary();

        $count = $diary->all()->count();
        $firstdate = $diary->all('created_at')->min('created_at');
        $lastdate = $diary->all('created_at')->max('created_at');

        return response()->json( ['count'=>$count,'firstdate'=>$firstdate,'lastdate'=>$lastdate], 200);
    }


